const sort = require('./intervalInsertionSort')

describe('intervalInsertionSort', () => {
    test('sort([62, 83, 18, 53, 7, 17, 95, 86, 47, 69, 25, 28], 5) = [17, 28, 18, 47, 7, 25, 83, 86, 53, 69, 62, 95]', () => {
      const list = [62, 83, 18, 53, 7, 17, 95, 86, 47, 69, 25, 28]
      sort(list, 5)
      expect(list).toEqual([17, 28, 18, 47, 7, 25, 83, 86, 53, 69, 62, 95])
    })
    test('sort([17,	28,	18,	47,	7, 25, 83, 86, 53, 69, 62, 95], 3) = [17, 7, 18, 47, 28, 25, 69, 62, 53, 83, 86, 95]', () => {
      const list = [17,	28,	18,	47,	7, 25, 83, 86, 53, 69, 62, 95]
      sort(list, 3)
      expect(list).toEqual([17, 7, 18, 47, 28, 25, 69, 62, 53, 83, 86, 95])
    })
    test('sort([17, 7, 18, 47, 28, 25, 69, 62, 53, 83, 86, 95], 1) = [7, 17, 18, 25, 28, 47, 53, 62, 69, 83, 86, 95]', () => {
      const list = [17, 7, 18, 47, 28, 25, 69, 62, 53, 83, 86, 95]
      sort(list, 1)
      expect(list).toEqual([7, 17, 18, 25, 28, 47, 53, 62, 69, 83, 86, 95])
    })
})
